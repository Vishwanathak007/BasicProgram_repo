package array_program_collection;

public class Program_to_use_Escape_Sequence 
{
	public static void main(String[] args) 
	{
		String str1 = "Testing in \t Infotrack";
		String str2 = "Testing in \n Infotrack";
		String str3 = "Testing in \b Infotrack";
		String str4 = "Testing in \' Infotrack";
		String str5 = "Testing in \"Infotrack\"";
		String str6 = "Testing in \\ Infotrack";
		
		System.out.println(str1);
		System.out.println(str2);
		System.out.println(str3);
		System.out.println(str4);
		System.out.println(str5);
		System.out.println(str6);
		
		System.out.println("\"Hello\"");
	}
}
